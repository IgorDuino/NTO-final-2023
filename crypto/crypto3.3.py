def inv(a, m):
    return pow(a, -1, m) 
      
def findMinX(num, rem, k) : 
    prod = 1
    for i in range(0, k) : 
        prod = prod * num[i] 
  
    result = 0

    for i in range(0,k): 
        pp = prod // num[i] 
        result = result + rem[i] * inv(pp, num[i]) * pp 
      
    return result % prod 

